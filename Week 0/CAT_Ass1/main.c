#include "avr/delay.h"
#include "Platform_Types.h"
#include "BIT_MATH.h"

// define the addresses of the registers
#define DDRA             *((vuint8*)0X3A)
#define PORTA           *((vuint8*)0X3B)
#define PINA           *((vuint8*)0X39)

void main (void)
{
    // set pin A0 as output
	DDRA |=(1<<0);

	while(1)
	{
		// turn on led for 1 sec
		  PORTA |=(1<<0);
		  _delay_ms(1000);
		// turn off led for 1 sec
		 PORTA &=~(1<<0);
		 _delay_ms(1000);

	}
}
